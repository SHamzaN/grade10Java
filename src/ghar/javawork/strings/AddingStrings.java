package ghar.javawork.strings;

public class AddingStrings {
    public static void main(String[] args){

    }
    public static int sumStrings(String one, String two, String three)
    {
        int i1 = Integer.parseInt(one);

        int i2 = Integer.parseInt(two);

        int i3 = Integer.parseInt(three);



        int sum = i1 + i2 + i3;

        return sum;

    }
}
